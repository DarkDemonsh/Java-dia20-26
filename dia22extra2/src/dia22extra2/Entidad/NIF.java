package dia22extra2.Entidad;

public class NIF {
   
    public long dni;
    public String l;

    public NIF() {
    }

    public NIF(long dni, String l) {
        this.dni = dni;
        this.l = l;
    }

    public long getDni() {
        return dni;
    }

    public void setDni(long dni) {
        this.dni = dni;
    }

    public String getL() {
        return l;
    }

    public void setL(String l) {
        this.l = l;
    }
    
    
    
}
